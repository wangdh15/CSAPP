/* 
 * bitXor - x^y using only ~ and & 
 *   Example: bitXor(4, 5) = 1
 *   Legal ops: ~ &
 *   Max ops: 14
 *   Rating: 1
 */
int bitXor(int x, int y) {
    /* x ^ y = (~x & y) | (~y & x)
     * x | y = ~((~x) & (~y))
     */
    int t1 = (~x) & y;
    int t2 = (~y) & x;
    return ~((~t1) & (~t2));
}
